create or replace function util.process_embeddings(
  batch_size integer default 3,
  max_requests integer default 3,
  timeout_milliseconds integer default ((5 * 60) * 1000)
) returns void
language plpgsql
set search_path to ''
as $$
declare
  job_batches jsonb[];
  batch jsonb;
  edge_fn text;
  timeout_seconds integer;
begin
  if batch_size <= 0 or max_requests <= 0 then
    return;
  end if;

  if not pg_try_advisory_xact_lock(hashtext('util.process_embeddings')) then
    return;
  end if;

  timeout_seconds := greatest(1, ceiling(timeout_milliseconds / 1000.0)::integer);

  with expired_jobs as (
    select
      q.msg_id,
      q.message,
      q.read_ct,
      p.max_read_count
    from pgmq.q_embedding_jobs q
    cross join lateral util.embedding_queue_policy_for(
      coalesce(q.message->>'schema', ''),
      coalesce(q.message->>'table', ''),
      coalesce(q.message->>'edgeFunction', 'embedding'),
      coalesce(q.message->>'embeddingColumn', '')
    ) p
    where q.vt <= clock_timestamp()
      and q.read_ct >= p.max_read_count
    order by q.msg_id
    limit greatest(batch_size * max_requests, 100)
  ),
  recorded_failures as (
    insert into util.embedding_job_failures (
      queue_name,
      msg_id,
      read_count,
      reason,
      message
    )
    select
      'embedding_jobs',
      e.msg_id,
      e.read_ct,
      format('read_ct reached retry cap %s', e.max_read_count),
      e.message
    from expired_jobs e
    returning msg_id
  )
  delete from pgmq.q_embedding_jobs q
  using recorded_failures f
  where q.msg_id = f.msg_id;

  with visible_jobs as (
    select
      q.msg_id,
      q.message,
      coalesce(q.message->>'schema', '') as schema_name,
      coalesce(q.message->>'table', '') as table_name,
      coalesce(q.message->>'edgeFunction', 'embedding') as edge_function,
      coalesce(q.message->>'embeddingColumn', '') as embedding_column
    from pgmq.q_embedding_jobs q
    where q.vt <= clock_timestamp()
  ),
  scoped_jobs as (
    select
      v.*,
      p.max_in_flight,
      active.active_count,
      row_number() over (
        partition by
          v.schema_name,
          v.table_name,
          v.edge_function,
          v.embedding_column
        order by v.msg_id
      ) as scope_position
    from visible_jobs v
    cross join lateral util.embedding_queue_policy_for(
      v.schema_name,
      v.table_name,
      v.edge_function,
      v.embedding_column
    ) p
    cross join lateral (
      select count(*)::integer as active_count
      from pgmq.q_embedding_jobs active
      where active.vt > clock_timestamp()
        and coalesce(active.message->>'schema', '') = v.schema_name
        and coalesce(active.message->>'table', '') = v.table_name
        and coalesce(active.message->>'edgeFunction', 'embedding') = v.edge_function
        and coalesce(active.message->>'embeddingColumn', '') = v.embedding_column
    ) active
    where p.mode = 'normal'
  ),
  selected_jobs as (
    select *
    from scoped_jobs
    where active_count < max_in_flight
      and scope_position <= (max_in_flight - active_count)
    order by msg_id
    limit (batch_size * max_requests)
  ),
  claimed_jobs as (
    update pgmq.q_embedding_jobs q
    set
      vt = clock_timestamp() + timeout_seconds * interval '1 second',
      read_ct = q.read_ct + 1
    from selected_jobs s
    where q.msg_id = s.msg_id
    returning
      q.msg_id,
      q.message || jsonb_build_object('jobId', q.msg_id) as job_info,
      coalesce(q.message->>'edgeFunction', 'embedding') as edge_function
  ),
  numbered_jobs as (
    select
      c.msg_id,
      c.job_info,
      c.edge_function,
      ((row_number() over (
        partition by c.edge_function
        order by c.msg_id
      ) - 1) / batch_size) as batch_num
    from claimed_jobs c
  ),
  batched_jobs as (
    select
      jsonb_agg(job_info order by msg_id) as batch_array,
      edge_function,
      batch_num
    from numbered_jobs
    group by edge_function, batch_num
    order by edge_function, batch_num
  )
  select array_agg(batch_array)
  from batched_jobs
  into job_batches;

  if job_batches is null then
    return;
  end if;

  foreach batch in array job_batches loop
    edge_fn := batch->0->>'edgeFunction';

    perform util.invoke_edge_function(
      name => edge_fn,
      body => batch,
      timeout_milliseconds => timeout_milliseconds
    );
  end loop;
end;
$$;

