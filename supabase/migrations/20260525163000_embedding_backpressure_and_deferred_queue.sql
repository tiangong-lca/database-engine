create table if not exists util.embedding_queue_policy (
  id bigint generated by default as identity primary key,
  scope_schema text not null default '*',
  scope_table text not null default '*',
  scope_edge_function text not null default '*',
  scope_embedding_column text not null default '*',
  mode text not null default 'normal',
  max_in_flight integer not null default 10,
  max_read_count integer not null default 20,
  retry_backoff_seconds integer not null default 300,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),
  constraint embedding_queue_policy_mode_check
    check (mode in ('normal', 'deferred', 'paused')),
  constraint embedding_queue_policy_scope_not_empty_check
    check (
      scope_schema <> ''
      and scope_table <> ''
      and scope_edge_function <> ''
      and scope_embedding_column <> ''
    ),
  constraint embedding_queue_policy_positive_limits_check
    check (
      max_in_flight > 0
      and max_read_count > 0
      and retry_backoff_seconds > 0
    )
);

create unique index if not exists embedding_queue_policy_scope_key
  on util.embedding_queue_policy (
    scope_schema,
    scope_table,
    scope_edge_function,
    scope_embedding_column
  );

create table if not exists util.pending_embedding_jobs (
  id bigint generated by default as identity primary key,
  schema_name text not null,
  table_name text not null,
  record_id text not null,
  record_version text not null,
  content_function text not null,
  embedding_column text not null,
  edge_function text not null,
  message jsonb not null,
  status text not null default 'pending',
  queue_msg_id bigint,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),
  enqueued_at timestamp with time zone,
  constraint pending_embedding_jobs_status_check
    check (status in ('pending', 'enqueued', 'discarded'))
);

create unique index if not exists pending_embedding_jobs_pending_scope_key
  on util.pending_embedding_jobs (
    schema_name,
    table_name,
    record_id,
    record_version,
    embedding_column,
    edge_function
  )
  where status = 'pending';

create table if not exists util.embedding_job_failures (
  id bigint generated by default as identity primary key,
  queue_name text not null default 'embedding_jobs',
  msg_id bigint,
  read_count integer,
  reason text not null,
  message jsonb not null,
  failed_at timestamp with time zone not null default now()
