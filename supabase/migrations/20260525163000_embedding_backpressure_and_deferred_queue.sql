create index if not exists embedding_job_failures_failed_at_idx
  on util.embedding_job_failures (failed_at desc);

insert into util.embedding_queue_policy (
  scope_schema,
  scope_table,
  scope_edge_function,
  scope_embedding_column,
  mode,
  max_in_flight,
  max_read_count,
  retry_backoff_seconds
)
values
  ('*', '*', '*', '*', 'normal', 10, 20, 300),
  ('public', 'flows', 'embedding_ft', 'embedding_ft', 'normal', 2, 20, 300)
on conflict (
  scope_schema,
  scope_table,
  scope_edge_function,
  scope_embedding_column
) do update
set
  mode = excluded.mode,
  max_in_flight = excluded.max_in_flight,
  max_read_count = excluded.max_read_count,
  retry_backoff_seconds = excluded.retry_backoff_seconds,
  updated_at = now();

create or replace function util.embedding_queue_policy_for(
  p_schema_name text,
  p_table_name text,
  p_edge_function text,
  p_embedding_column text
)
returns table (
  mode text,
  max_in_flight integer,
  max_read_count integer,
  retry_backoff_seconds integer
)
language sql
stable
set search_path to ''
as $$
  select
    p.mode,
    p.max_in_flight,
    p.max_read_count,
    p.retry_backoff_seconds
  from util.embedding_queue_policy p
  where p.scope_schema in ('*', coalesce(p_schema_name, ''))
    and p.scope_table in ('*', coalesce(p_table_name, ''))
    and p.scope_edge_function in ('*', coalesce(p_edge_function, 'embedding'))
    and p.scope_embedding_column in ('*', coalesce(p_embedding_column, ''))
  order by
    (
      (p.scope_schema <> '*')::integer
      + (p.scope_table <> '*')::integer
      + (p.scope_edge_function <> '*')::integer
      + (p.scope_embedding_column <> '*')::integer
    ) desc,
    p.updated_at desc,
    p.id desc
  limit 1
$$;

