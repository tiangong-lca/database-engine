create or replace function util.queue_embeddings() returns trigger
language plpgsql
security definer
set search_path to ''
as $$
declare
  v_content_function text = TG_ARGV[0];
  v_embedding_column text = TG_ARGV[1];
  v_edge_function text := coalesce(TG_ARGV[2], 'embedding');
  queue_mode text;
  v_message jsonb;
begin
  v_message := jsonb_build_object(
    'id', NEW.id,
    'version', NEW.version,
    'schema', TG_TABLE_SCHEMA,
    'table', TG_TABLE_NAME,
    'contentFunction', v_content_function,
    'embeddingColumn', v_embedding_column,
    'edgeFunction', v_edge_function
  );

  select p.mode
    into queue_mode
  from util.embedding_queue_policy_for(
    TG_TABLE_SCHEMA,
    TG_TABLE_NAME,
    v_edge_function,
    v_embedding_column
  ) p;

  if queue_mode in ('deferred', 'paused') then
    insert into util.pending_embedding_jobs (
      schema_name,
      table_name,
      record_id,
      record_version,
      content_function,
      embedding_column,
      edge_function,
      message
    )
    values (
      TG_TABLE_SCHEMA,
      TG_TABLE_NAME,
      NEW.id::text,
      NEW.version::text,
      v_content_function,
      v_embedding_column,
      v_edge_function,
      v_message
    )
    on conflict (
      schema_name,
      table_name,
      record_id,
      record_version,
      embedding_column,
      edge_function
    )
    where status = 'pending'
    do update
    set
      content_function = excluded.content_function,
      message = excluded.message,
      updated_at = now();

    return NEW;
  end if;

  perform pgmq.send(
    queue_name => 'embedding_jobs',
    msg => v_message
  );

  return NEW;
end;
$$;

alter function util.queue_embeddings() owner to postgres;

create or replace function util.enqueue_pending_embeddings(
  p_limit integer default 100,
  p_schema_name text default null,
  p_table_name text default null,
  p_edge_function text default null,
  p_embedding_column text default null
) returns integer
language plpgsql
security definer
set search_path to ''
as $$
declare
  pending_job record;
  sent_count integer := 0;
  sent_msg_id bigint;
begin
  if p_limit <= 0 then
    return 0;
  end if;

  if not pg_try_advisory_xact_lock(hashtext('util.enqueue_pending_embeddings')) then
    return 0;
  end if;

  for pending_job in
    select pending.*
    from util.pending_embedding_jobs pending
    cross join lateral util.embedding_queue_policy_for(
      pending.schema_name,
      pending.table_name,
      pending.edge_function,
      pending.embedding_column
    ) p
    where pending.status = 'pending'
      and p.mode <> 'paused'
      and (p_schema_name is null or pending.schema_name = p_schema_name)
      and (p_table_name is null or pending.table_name = p_table_name)
      and (p_edge_function is null or pending.edge_function = p_edge_function)
      and (p_embedding_column is null or pending.embedding_column = p_embedding_column)
    order by pending.created_at, pending.id
    limit p_limit
    for update skip locked
  loop
    select pgmq.send(
      queue_name => 'embedding_jobs',
      msg => pending_job.message
    )
    into sent_msg_id;

    update util.pending_embedding_jobs
    set
      status = 'enqueued',
      queue_msg_id = sent_msg_id,
      enqueued_at = now(),
      updated_at = now()
    where id = pending_job.id;

    sent_count := sent_count + 1;
  end loop;

  return sent_count;
end;
$$;

